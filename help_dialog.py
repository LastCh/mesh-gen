"""Окно подсказок и описания параметров."""

from PyQt5.QtWidgets import QDialog, QVBoxLayout, QTabWidget, QTextBrowser
from PyQt5.QtCore import Qt


class HelpDialog(QDialog):
    """Диалог с описанием параметров и функций приложения."""

    def __init__(self, parent=None):
        super().__init__(parent)
        self.setWindowTitle("Помощь")
        self.resize(520, 560)
        self.setWindowModality(Qt.NonModal)
        layout = QVBoxLayout()

        tabs = QTabWidget()
        tabs.addTab(self._create_tab(self._stft_text()), "STFT")
        tabs.addTab(self._create_tab(self._visual_text()), "Визуализация")
        tabs.addTab(self._create_tab(self._controls_text()), "Управление")

        layout.addWidget(tabs)
        self.setLayout(layout)

    def _create_tab(self, html):
        browser = QTextBrowser()
        browser.setOpenExternalLinks(True)
        browser.setHtml(html)
        return browser

    def _stft_text(self):
        return """
        <h3>Параметры STFT</h3>
        <p><b>N_FFT</b> — количество отсчётов в окне БПФ. Чем выше значение, тем
        выше частотное разрешение, но тем хуже видны быстрые изменения по времени.</p>
        <p><b>Hop Length</b> — шаг окна. Малое значение даёт более плавную анимацию и
        лучшую точность по времени, но увеличивает число кадров и нагрузку.</p>
        <p><b>Тип окна</b> влияет на подавление спектральных утечек:</p>
        <ul>
            <li><b>Hann</b> — универсальное сглаживание.</li>
            <li><b>Hamming</b> — похож на Hann, но оставляет чуть больше высоких частот.</li>
            <li><b>Blackman</b> — сильнее подавляет боковые лепестки, но хуже разрешение.</li>
            <li><b>Bartlett</b> — треугольное окно, компромисс по шуму.</li>
        </ul>
        <p><b>Глобальная нормализация</b> приводит амплитуды всех кадров к одному диапазону,
        что облегчает сравнение разных участков.</p>
        """

    def _visual_text(self):
        return """
        <h3>Визуализация</h3>
        <p><b>Макс. частота</b> ограничивает диапазон отображаемого спектра. Это не
        пересчитывает STFT, а лишь скрывает высокие полосы.</p>
        <p><b>Усиление</b> масштабирует высоту меша. При слишком больших значениях
        возможна «обрезка» вершин.</p>
        <p><b>Размер окна</b> задаёт количество кадров, попадающих в текущий сегмент.
        Чем больше окно, тем длиннее по времени фрагмент визуализации.</p>
        <p><b>Цветовая карта</b> и диапазон dB управляют раскраской амплитуды. Можно
        оставить глобальные значения или зафиксировать собственные пределы.</p>
        """

    def _controls_text(self):
        return """
        <h3>Основные действия</h3>
        <ul>
            <li><b>Загрузка</b> — выбирает WAV/MP3, автоматически готовит данные STFT.</li>
            <li><b>Play/Pause/Stop</b> — управление воспроизведением синхронно с 3D-сценой.
                При остановке визуализация очищается и строится заново после нажатия Play.</li>
            <li><b>Таймлайн</b> — можно перетянуть для быстрой перемотки.</li>
            <li><b>Полная спектрограмма</b> — открывает 2D-вид и позволяет выбрать сегмент.</li>
            <li><b>Экспорт меша</b> — сохраняет текущую геометрию в VTK/OBJ/STL.</li>
        </ul>
        <p><b>Пресеты камеры</b> автоматически подбирают позицию так, чтобы весь меш
        попадал в кадр. Можно сохранить пользовательскую позицию при закрытии.</p>
        <p>Горячие клавиши: Space — Play/Pause, Ctrl+S — стоп, Ctrl+O — открыть файл,
        1–4 — пресеты камер.</p>
        """

